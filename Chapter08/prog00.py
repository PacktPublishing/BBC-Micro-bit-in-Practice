with open('HenryV.txt', 'w') as file_handle:
    file_handle.write("We few, we happy few, we band of brothers;")
    file_handle.write("\nFor he to-day that sheds his blood with me")
    file_handle.write("\nShall be my brother; be he ne'er so vile,")
    file_handle.write("\nThis day shall gentle his condition;")
    file_handle.write("\nAnd gentlemen in England now a-bed")
    file_handle.write("\nShall think themselves accurs'd they were not here,")
    file_handle.write("\nAnd hold their manhoods cheap whiles any speaks")
    file_handle.write("\nThat fought with us upon Saint Crispin's day.")
